USE DA2;

CREATE TABLE USERS (
    ID            int(5)          NOT NULL,
    FirstName     varchar(45)     NOT NULL,
    LastName      varchar(45)     NOT NULL,
    Username      varchar(45)     NOT NULL,
    Passwd        varchar(45)     NOT NULL,
    PhoneNumber   varchar(45)     NOT NULL,
    Carrier       varchar(45)     NOT NULL,
    Email         varchar(45)     NOT NULL

);